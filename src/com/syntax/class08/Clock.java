package com.syntax.class08;

public class Clock {

	public static void main(String[] args) {
		
		
		
		for(int i=1;i<=4;i++) {
			for(int j=1;j<=5;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		for(int a=1;a<=10;a++) {
			for(int b=1;b<=10;b++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		for(int c=1;c<=4;c++) {
			for(int d=1;d<=5;d++) {
				System.out.print(d);
			}
			System.out.println();
		}
		
		System.out.println("--------------------");
		
		for(int e=1;e<=4;e++) {
			for(int f=1;f<=5;f++) {
				System.out.print(e);
			}
			System.out.println();
		}
		
		System.out.println("--------------------------");
		
		for(int g=5;g>=1;g--) {
			for(int h=1;h<=4;h++) {
				System.out.print(g+" ");
			}
			System.out.println();
			
		}
		
		
		
		
		
		
		
	}

}
