package com.syntax.class06;

public class WhileLOOPs {

	public static void main(String[] args) {
		
		//Print numbers from 1 to 100 in 1 line with space
		
		int i=1;
		
		while(i<101) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println("-----------------------------------------------------------------------");
        //Print numbers from 100 to 1
        int x=100;
		
		while(x>0) {
			System.out.print(x+" ");
			x--;
		}
	}

}
